package com.suren.shares;

public class Share {

	String name;
	String link;
	String bookValue; 
	String marketCapital = null;
	String eps = null;
	String profitByEquity = null;
	String dividend = null;
	String dividendYield = null; 
	String marketLot = null;  
	String faceValue = null;  
	String industry = null; 
	String priceByBook = null;   
	String profitByCapital = null; 
	
	  
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getBookValue() {
		return bookValue;
	}
	public void setBookValue(String bookValue) {
		this.bookValue = bookValue;
	}
	public String getMarketCapital() {
		return marketCapital;
	}
	public void setMarketCapital(String marketCapital) {
		this.marketCapital = marketCapital;
	}
	public String getEps() {
		return eps;
	}
	public void setEps(String eps) {
		this.eps = eps;
	}
	public String getProfitByEquity() {
		return profitByEquity;
	}
	public void setProfitByEquity(String profitByEquity) {
		this.profitByEquity = profitByEquity;
	}
	public String getDividend() {
		return dividend;
	}
	public void setDividend(String dividend) {
		this.dividend = dividend;
	}
	public String getDividendYield() {
		return dividendYield;
	}
	public void setDividendYield(String dividendYield) {
		this.dividendYield = dividendYield;
	}
	public String getMarketLot() {
		return marketLot;
	}
	public void setMarketLot(String marketLot) {
		this.marketLot = marketLot;
	}
	public String getFaceValue() {
		return faceValue;
	}
	public void setFaceValue(String faceValue) {
		this.faceValue = faceValue;
	}
	public String getIndustry() {
		return industry;
	}
	public void setIndustry(String industry) {
		this.industry = industry;
	}
	public String getPriceByBook() {
		return priceByBook;
	}
	public void setPriceByBook(String priceByBook) {
		this.priceByBook = priceByBook;
	}
	public String getProfitByCapital() {
		return profitByCapital;
	}
	public void setProfitByCapital(String profitByCapital) {
		this.profitByCapital = profitByCapital;
	}
	
	
	
}
